-- MAX - VALOR MÁXIMO DE UMA COLUNA
SELECT MAX(FEVEREIRO) AS MAIOR_FEV FROM VENDEDORES;

-- MIN - VALOR MÍNIMO DE UMA COLUNA
SELECT MIN(FEVEREIRO) AS MIN_FEV FROM VENDEDORES;


-- AVG - VALOR MÉDIO DE UMA COLUNA
SELECT AVG(FEVEREIRO) AS MÉDIA FROM VENDEDORES;


-- JUNTANDO AS FUNÇÕES 
SELECT MAX(JANEIRO) AS MAX_JAN, MIN(JANEIRO) AS MIN_JAN, TRUNCATE(AVG(JANEIRO),2) AS MED_JAN
FROM VENDEDORES;

-- AGREGAÇÃO COM SUM()
SELECT SUM(JANEIRO) AS TOTAL_JAN
FROM VENDEDORES;

SELECT SUM(JANEIRO) AS TOTAL_JANEIRO, 
	   SUM(FEVEREIRO) AS TOTAL_FEVEREIRO,
       SUM(MARCO) AS TOTAL_MARCO
FROM VENDEDORES;

-- VENDAS POR SEXO
SELECT SEXO, SUM(MARCO) AS TOTAL_MARCO
FROM VENDEDORES
GROUP BY SEXO;

